package com.bl.factors;

import java.util.Scanner;

public class Factors {
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter a number::");
		int NUM = sc.nextInt();

		for (int i = 1; i <= i; i++) {
			if (NUM % i == 0) {
				System.out.println(i+ "");
			}
		}
		sc.close();
	}

}
